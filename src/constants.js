// ランダム微変動
export const nudge = (v) => Math.max(1000, Math.min(9999, Math.round(v + (Math.random()-0.5)*12)));

// 市民ID生成
export const randId = () => "IK-2026-" + String(Math.floor(1000+Math.random()*9000));

// インフラ維持税計算 (1.5%)
export const calcTax = (priceStr) => {
  const num = parseInt((priceStr||"0").replace(/[¥,]/g,""), 10) || 0;
  const tax = Math.round(num * 0.015);
  return { total: "¥" + (num+tax).toLocaleString(), tax: "¥" + tax.toLocaleString() };
};

// 市民ID形式バリデーション: IK-YYYY-NNNN
export const validateCitizenId = (id) => /^IK-\d{4}-\d{4}$/.test(id.trim());

// ログシーケンス実行 (600ms間隔、最後のログ後+2秒待機)
export const runSequence = (seq, setLogs, onDone) => {
  setLogs([]);
  let i = 0;
  const INTERVAL = 600;
  const FINAL_DELAY = 2000;
  const step = () => {
    if (i >= seq.length) {
      setTimeout(onDone, FINAL_DELAY);
      return;
    }
    setLogs((p) => [...p, seq[i]]);
    i++;
    setTimeout(step, INTERVAL);
  };
  setTimeout(step, 200);
};
